// The site ships 9 locales (no separate `ja` locale). Japanese sign tokens
// (出口/西口/中央出口…) are embedded directly in every locale's copy, so the
// teaching content is bilingual regardless of the active UI language.
for (const file of fs.readdirSync(DIR)) {
  if (!file.endsWith(".json")) continue;
  const locale = file.replace(/\.json$/, "");
  if (!ui[locale] || !mission[locale]) continue;
  const full = path.join(DIR, file);
  const json = JSON.parse(fs.readFileSync(full, "utf8"));
  json.stationPractice = {
    ui: ui[locale],
    mission: mission[locale],
  };
  fs.writeFileSync(full, JSON.stringify(json, null, 2) + "\n", "utf8");
  console.log(`updated ${file}`);
}
